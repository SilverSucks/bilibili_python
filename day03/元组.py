# 如果创建的元组中只有一个元素，默认为这个元素的基本类型
tuple2 = ('第二个元组')  # 此时没有加逗号
print('当元组中只有一个元素"第二个元组"时不加逗号，判断其类型为；', type(tuple2))
tuple3 = (99,)
